package cn.edu.bistu.spider.model;

public class WeiboModel {
	String id;
	String userid;
	String content;
	String like;
	String transfer;
	String comment;
	String time;
	String platform;
	String repostusers;
	String commentusers;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getLike() {
		return like;
	}
	public void setLike(String like) {
		this.like = like;
	}
	public String getTransfer() {
		return transfer;
	}
	public void setTransfer(String transfer) {
		this.transfer = transfer;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getPlatform() {
		return platform;
	}
	public void setPlatform(String platform) {
		this.platform = platform;
	}
	public String getRepostusers() {
		return repostusers;
	}
	public void setRepostusers(String repostusers) {
		this.repostusers = repostusers;
	}
	public String getCommentusers() {
		return commentusers;
	}
	public void setCommentusers(String commentusers) {
		this.commentusers = commentusers;
	}
	
}
